# STATEMENTS 

# Basic if statement
if condition:
    code

# Basic if else statement 
if condition:
    code
else:
    code

# Basic if else statement 
if condition:
    code
elif condition:
    code
elif condition:
    code

# Nested statements
if condition:
    if condition:
        code
    elif condition:
        code
    else:
        code
elif condition:
    code
elif condition:
    code
