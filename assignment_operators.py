#Assisgnment operators

#add i.e. first_number=first_number+second_number
first_number+=second_number		

#subtract i.e. first_number=first_number-second_number
first_number-=second_number

#multiply i.e. first_number=first_number*second_number
first_number*=second_number

#power i.e. first_number=first_number**second_number
first_number**=second_number						

#divide remainder i.e. first_number=first_number%second_number
first_number%=second_number		

#quotient remainder i.e. first_number=first_number/second_number
first_number/=second_number		

	

