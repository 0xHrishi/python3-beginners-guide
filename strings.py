#String examples 
full_name = "Hrishi"

lucky_number = 8

#String concatenation
print("Your full name is " + full_name + " and lucky number is " + str(lucky_number))

#Formatted output
print(f"Your full name is {full_name} and lucky number is {lucky_number}")

#String slicing 
#string[start:end:step]
print(full_name[0:3])
#Will start from index 0
print(full_name[:3])
#From index 2 till the end
print(full_name[2:])
